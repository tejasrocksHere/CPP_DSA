// JavaScript is a dynamic language unlike C++/Java, where we can reassign any data type to a variable

// Initializing a variable x with a string value
let x = "tejas";
console.log(x); // Output: tejas
console.log(typeof(x)); // Output: string

// Reassigning the variable x to a number
x = 3;
console.log(x); // Output: 3
console.log(typeof(x)); // Output: number

// Declaring a variable y without assigning a value
let y;
console.log(y); // Output: undefined
console.log(typeof(y)); // Output: undefined

// Initializing a variable k with a floating-point number
let k = 1.2;
console.log(k); // Output: 1.2
console.log(typeof(k)); // Output: number

// Reassigning the variable k to an integer
k = 1;
console.log(k); // Output: 1
console.log(typeof(k)); // Output: number
